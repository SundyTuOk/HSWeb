package com.sf.energy.map.model;

public class MapDistributeModel {
   //图例标识
	private String LableList_ID;
	//数据点的标识
	private String Date_ID;
	//备注
	private String Remark;
	//数据点X轴坐标
	private String PointX;
	//数据点Y轴坐标
	private String PointY;
	public String getLableList_ID() {
		return LableList_ID;
	}
	public void setLableList_ID(String lableList_ID) {
		LableList_ID = lableList_ID;
	}
	public String getDate_ID() {
		return Date_ID;
	}
	public void setDate_ID(String date_ID) {
		Date_ID = date_ID;
	}
	public String getRemark() {
		return Remark;
	}
	public void setRemark(String remark) {
		Remark = remark;
	}
	public String getPointX() {
		return PointX;
	}
	public void setPointX(String pointX) {
		PointX = pointX;
	}
	public String getPointY() {
		return PointY;
	}
	public void setPointY(String pointY) {
		PointY = pointY;
	}
	
	
}
