package com.sf.energy.co2sale.model;

import java.util.Date;

public class CO2SaleInfoModel {
	/**
	 * 部门ID
	 */
	private int partID;
	
	/**
	 * 部门名称
	 */
	private String partName;
	
	/** 
	
	* @Fields buytime : 购买时间
	
	*/ 
	private String buytime;
	
	/** 
	
	* @Fields areaID : 区域ID
	
	*/ 
	private int areaID;
	
	/** 
	
	* @Fields areaName : 区域名称
	
	*/ 
	private String areaName;
	
	/**
	 * 购买数量
	 */
	private String buyValue;
	
	/** 
	
	* @Fields remainingValue : 剩余用量
	
	*/ 
	private String remainingValue;

	
	/** 
	
	* @Fields SaleOrBuy : 买入还是卖出(用一句话描述这个变量表示什么) 
	
	*/ 
	private int SaleOrBuy;
	
	
	
	public int getSaleOrBuy()
	{
		return SaleOrBuy;
	}

	public void setSaleOrBuy(int saleOrBuy)
	{
		SaleOrBuy = saleOrBuy;
	}

	public int getPartID() {
		return partID;
	}

	public void setPartID(int partID) {
		this.partID = partID;
	}

	public String getPartName() {
		return partName;
	}

	public void setPartName(String partName) {
		this.partName = partName;
	}

	public String getBuyValue() {
		return buyValue;
	}

	public void setBuyValue(String buyValue) {
		this.buyValue = buyValue;
	}

	public String getRemainingValue() {
		return remainingValue;
	}

	public void setRemainingValue(String remainingValue) {
		this.remainingValue = remainingValue;
	}

	public int getAreaID() {
		return areaID;
	}

	public void setAreaID(int areaID) {
		this.areaID = areaID;
	}

	public String getAreaName() {
		return areaName;
	}

	public void setAreaName(String areaName) {
		this.areaName = areaName;
	}

	public String getBuytime() {
		return buytime;
	}

	public void setBuytime(String buytime) {
		this.buytime = buytime;
	}
}
