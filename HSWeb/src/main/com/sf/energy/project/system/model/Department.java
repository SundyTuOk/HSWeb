package com.sf.energy.project.system.model;

/**
 * <部门实体类，并提供了set和get方法>
 * 
 * @author 李涵淼
 * @version 1.0
 * @since[version 1.0]
 * 
 */
public class Department
{
	// / 部门ID
	private int departmentID;

	// / 部门编号
	private String departmentNum;

	// / 部门名称
	private String departmetName;

	// / 部门父组织ID
	private int departmentParentID;

	// / 部门负责人
	private String departmentMan;

	// / 部门电话
	private String departmentPhone;

	// / 部门备注
	private String departmentRemark;

	// / 部门加入上级节点的显示
	private String departmentText;

	// / 部门显示的排序字段
	private int departmentOrder;

	// / 部门完整ID（包括父节点ID）
	private String departmentIDs;

	public Department(int departmentID, String departmentNum,
			String departmentName, int departmentParentID,
			String departmentMan, String departmentPhone,
			String departmentRemark, int departmentOrder,
			String departmentText, String departmentIDs)
	{
		this.departmentID = departmentID;
		this.departmentNum = departmentNum;
		this.departmetName = departmentName;
		this.departmentParentID = departmentParentID;
		this.departmentMan = departmentMan;
		this.departmentPhone = departmentPhone;
		this.departmentRemark = departmentRemark;
		this.departmentText = departmentText;
		this.departmentOrder = departmentOrder;
		this.departmentIDs = departmentIDs;
	}

	public Department()
	{

	}

	/**
	 * 
	 * get和set方法
	 */
	public int getDepartmentID()
	{
		return departmentID;
	}

	public void setDepartmentID(int departmentID)
	{
		this.departmentID = departmentID;
	}

	public String getDepartmentNum()
	{
		return departmentNum;
	}

	public void setDepartmentNum(String departmentNum)
	{
		this.departmentNum = departmentNum;
	}

	public String getDepartmetName()
	{
		return departmetName;
	}

	public void setDepartmetName(String departmetName)
	{
		this.departmetName = departmetName;
	}

	public int getDepartmentParentID()
	{
		return departmentParentID;
	}

	public void setDepartmentParentID(int departmentParentID)
	{
		this.departmentParentID = departmentParentID;
	}

	public String getDepartmentMan()
	{
		return departmentMan;
	}

	public void setDepartmentMan(String departmentMan)
	{
		this.departmentMan = departmentMan;
	}

	public String getDepartmentPhone()
	{
		return departmentPhone;
	}

	public void setDepartmentPhone(String departmentPhone)
	{
		this.departmentPhone = departmentPhone;
	}

	public String getDepartmentRemark()
	{
		return departmentRemark;
	}

	public void setDepartmentRemark(String departmentRemark)
	{
		this.departmentRemark = departmentRemark;
	}

	public String getDepartmentText()
	{
		return departmentText;
	}

	public void setDepartmentText(String departmentText)
	{
		this.departmentText = departmentText;
	}

	public int getDepartmentOrder()
	{
		return departmentOrder;
	}

	public void setDepartmentOrder(int departmentOrder)
	{
		this.departmentOrder = departmentOrder;
	}

	public String getDepartmentIDs()
	{
		return departmentIDs;
	}

	public void setDepartmentIDs(String departmentIDs)
	{
		this.departmentIDs = departmentIDs;
	}

}
