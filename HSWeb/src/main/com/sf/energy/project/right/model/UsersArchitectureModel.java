package com.sf.energy.project.right.model;

/**
 * 数据表【OprArc_List】的实例类
 *【OprArc_List】表记录用户的建筑权限
 * @author 鄢浩
 * @version 1.0 
 * @since version 1.0
 */
public class UsersArchitectureModel
{
	///用户ID
	private int usersID;
	
	///建筑ID
	private int ArchID;
	
	///区域ID
	private int areaID;

	
	
	
	public int getUsersID()
	{
		return usersID;
	}

	public void setUsersID(int usersID)
	{
		this.usersID = usersID;
	}

	public int getArchID()
	{
		return ArchID;
	}

	public void setArchID(int archID)
	{
		ArchID = archID;
	}

	public int getAreaID()
	{
		return areaID;
	}

	public void setAreaID(int areaID)
	{
		this.areaID = areaID;
	}
		
}
