package com.sf.energy.project.right.model;

/**
 * 数据表【Roles】的实例类
 *
 * @author 鄢浩
 * @version 1.0
 * @since version 1.0
 */
public class RolesModel
{
	/// 角色编号
	private int rolesID;
	
	/// 角色姓名
	private String rolesName;
	
	/// 是否执行
	private int rolesEnable;
	
	/// 备注
	private String rolesRemark;

	public int getRolesID()
	{
		return rolesID;
	}

	public void setRolesID(int rolesID)
	{
		this.rolesID = rolesID;
	}

	public String getRolesName()
	{
		return rolesName;
	}

	public void setRolesName(String rolesName)
	{
		this.rolesName = rolesName;
	}

	public int getRolesEnable()
	{
		return rolesEnable;
	}

	public void setRolesEnable(int rolesEnable)
	{
		this.rolesEnable = rolesEnable;
	}

	public String getRolesRemark()
	{
		return rolesRemark;
	}

	public void setRolesRemark(String rolesRemark)
	{
		this.rolesRemark = rolesRemark;
	}

}
