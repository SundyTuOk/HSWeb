package com.sf.energy.manager.current.model;

import java.util.Date;

public class AmMaintence
{
    int maintID = 0;
    int ammeterID = 0;
    String ammeterName = null;
    Date mainTime = null;
    String maintRemark = null;
    String maintMan = null;

    public String getAmmeterName()
    {
        return ammeterName;
    }

    public void setAmmeterName(String ammeterName)
    {
        this.ammeterName = ammeterName;
    }

    public int getMaintID()
    {
        return maintID;
    }

    public void setMaintID(int maintID)
    {
        this.maintID = maintID;
    }

    public int getAmmeterID()
    {
        return ammeterID;
    }

    public void setAmmeterID(int ammeterID)
    {
        this.ammeterID = ammeterID;
    }

    public Date getMainTime()
    {
        return mainTime;
    }

    public void setMainTime(Date mainTime)
    {
        this.mainTime = mainTime;
    }

    public String getMaintRemark()
    {
        return maintRemark;
    }

    public void setMaintRemark(String maintRemark)
    {
        this.maintRemark = maintRemark;
    }

    public String getMaintMan()
    {
        return maintMan;
    }

    public void setMaintMan(String maintMan)
    {
        this.maintMan = maintMan;
    }

}
