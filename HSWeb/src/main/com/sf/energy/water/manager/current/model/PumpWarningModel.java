package com.sf.energy.water.manager.current.model;

public class PumpWarningModel
{
	private String PumpWarning_ID = "";
	private String PumpWarning_NAME = "";
	private String PumpWarning_STYLE = "";
	private String PumpWarning_TIME = "";
	private String PumpWarning_REMARK = "";
	public String getPumpWarning_ID()
	{
		return PumpWarning_ID;
	}
	public void setPumpWarning_ID(String pumpWarning_ID)
	{
		PumpWarning_ID = pumpWarning_ID;
	}
	public String getPumpWarning_NAME()
	{
		return PumpWarning_NAME;
	}
	public void setPumpWarning_NAME(String pumpWarning_NAME)
	{
		PumpWarning_NAME = pumpWarning_NAME;
	}
	public String getPumpWarning_STYLE()
	{
		return PumpWarning_STYLE;
	}
	public void setPumpWarning_STYLE(String pumpWarning_STYLE)
	{
		PumpWarning_STYLE = pumpWarning_STYLE;
	}
	public String getPumpWarning_TIME()
	{
		return PumpWarning_TIME;
	}
	public void setPumpWarning_TIME(String pumpWarning_TIME)
	{
		PumpWarning_TIME = pumpWarning_TIME;
	}
	public String getPumpWarning_REMARK()
	{
		return PumpWarning_REMARK;
	}
	public void setPumpWarning_REMARK(String pumpWarning_REMARK)
	{
		PumpWarning_REMARK = pumpWarning_REMARK;
	}
}
