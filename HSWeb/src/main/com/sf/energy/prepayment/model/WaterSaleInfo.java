package com.sf.energy.prepayment.model;

public class WaterSaleInfo
{
	Integer ID;
	Integer WATERMETER_ID;
	String BUYTIME;
	Float LASTREMAINMONEY;
	Float THISUSEVOL;
	Float THISUSEMONEY;
	Float THISPURCHASEMONEY;
	Float THISTOTALMONEY;
	Integer USERS_ID;
	public Integer getID()
	{
		return ID;
	}
	public void setID(Integer iD)
	{
		ID = iD;
	}
	public Integer getWATERMETER_ID()
	{
		return WATERMETER_ID;
	}
	public void setWATERMETER_ID(Integer wATERMETER_ID)
	{
		WATERMETER_ID = wATERMETER_ID;
	}
	public String getBUYTIME()
	{
		return BUYTIME;
	}
	public void setBUYTIME(String bUYTIME)
	{
		BUYTIME = bUYTIME;
	}
	public Float getLASTREMAINMONEY()
	{
		return LASTREMAINMONEY;
	}
	public void setLASTREMAINMONEY(Float lASTREMAINMONEY)
	{
		LASTREMAINMONEY = lASTREMAINMONEY;
	}
	public Float getTHISUSEVOL()
	{
		return THISUSEVOL;
	}
	public void setTHISUSEVOL(Float tHISUSEVOL)
	{
		THISUSEVOL = tHISUSEVOL;
	}
	public Float getTHISUSEMONEY()
	{
		return THISUSEMONEY;
	}
	public void setTHISUSEMONEY(Float tHISUSEMONEY)
	{
		THISUSEMONEY = tHISUSEMONEY;
	}
	public Float getTHISPURCHASEMONEY()
	{
		return THISPURCHASEMONEY;
	}
	public void setTHISPURCHASEMONEY(Float tHISPURCHASEMONEY)
	{
		THISPURCHASEMONEY = tHISPURCHASEMONEY;
	}
	public Float getTHISTOTALMONEY()
	{
		return THISTOTALMONEY;
	}
	public void setTHISTOTALMONEY(Float tHISTOTALMONEY)
	{
		THISTOTALMONEY = tHISTOTALMONEY;
	}
	public Integer getUSERS_ID()
	{
		return USERS_ID;
	}
	public void setUSERS_ID(Integer uSERS_ID)
	{
		USERS_ID = uSERS_ID;
	}
	@Override
	public String toString()
	{
		return "WaterSaleInfo [ID=" + ID + ", WATERMETER_ID=" + WATERMETER_ID + ", BUYTIME=" + BUYTIME + ", LASTREMAINMONEY=" + LASTREMAINMONEY
				+ ", THISUSEVOL=" + THISUSEVOL + ", THISUSEMONEY=" + THISUSEMONEY + ", THISPURCHASEMONEY=" + THISPURCHASEMONEY + ", THISTOTALMONEY="
				+ THISTOTALMONEY + ", USERS_ID=" + USERS_ID + "]";
	}
	public WaterSaleInfo()
	{
		super();
	}
	public WaterSaleInfo(Integer iD, Integer wATERMETER_ID, String bUYTIME, Float lASTREMAINMONEY, Float tHISUSEVOL, Float tHISUSEMONEY,
			Float tHISPURCHASEMONEY, Float tHISTOTALMONEY, Integer uSERS_ID)
	{
		super();
		ID = iD;
		WATERMETER_ID = wATERMETER_ID;
		BUYTIME = bUYTIME;
		LASTREMAINMONEY = lASTREMAINMONEY;
		THISUSEVOL = tHISUSEVOL;
		THISUSEMONEY = tHISUSEMONEY;
		THISPURCHASEMONEY = tHISPURCHASEMONEY;
		THISTOTALMONEY = tHISTOTALMONEY;
		USERS_ID = uSERS_ID;
	}

}
