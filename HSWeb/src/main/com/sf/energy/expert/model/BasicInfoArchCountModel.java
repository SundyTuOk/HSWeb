package com.sf.energy.expert.model;

public class BasicInfoArchCountModel {

	String  styleNum;	//建筑分类标识
	String  styleName;	//建筑分类名称
	int     arcCount;	//分类建筑下面建筑的个数
	
	
	public String getStyleNum() {
		return styleNum;
	}
	public void setStyleNum(String styleNum) {
		this.styleNum = styleNum;
	}
	public String getStyleName() {
		return styleName;
	}
	public void setStyleName(String styleName) {
		this.styleName = styleName;
	}
	public int getArcCount() {
		return arcCount;
	}
	public void setArcCount(int arcCount) {
		this.arcCount = arcCount;
	}
	
	
}
